#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t		j;

	i = 0;
	j = 0;
	while (s1[i])
		++i;
	while ((s1[i++] = s2[j++]))
		;
	return (s1);
}
