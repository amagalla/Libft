#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		j;

	i = 0;
	while (s1[i])
		i++;
	j = i;
	while (s2[i - j] && i - j < n)
	{
		s1[i] = s2[i - j];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
